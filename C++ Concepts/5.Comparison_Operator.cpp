#include<iostream>
using namespace std;
int main(){
	cout<<"Comparison Operators\n";
	int a=3;
	int b=4;
	cout<<"The value of a is a==b "<<(a==b)<<endl;
	cout<<"The value of a is a!=b "<<(a!=b)<<endl;
	cout<<"The value of a is a>=b "<<(a>=b)<<endl;
	cout<<"The value of a is a<=b "<<(a<=b)<<endl;
	cout<<"The value of a is a>b "<<(a>b)<<endl;
	cout<<"The value of a is a<b "<<(a<b)<<endl;
}
